package com.nandan.command;
public class GarageDoor
{
    public GarageDoor()
    {

    }
    public void up()
    {
        System.out.println("Door is upped");
    }
    public void down()
    {
        System.out.println("Door is downed");
    }
    public void stop()
    {
        System.out.println("Door is stopped");
    }
    public void lightOn()
    {
        System.out.println("Door light is switched on");
    }
    public void lightOff()
    {
        System.out.println("Door light is switched off");
    }
}